// ── Core translate function ──────────────────────────────────────────────────
function translate(key, lang, vars) {
  const l = translations[lang] || translations.en
  const parts = key.split('.')
  let v = l
  for (const p of parts) {
    if (v == null) break
    v = v[p]
  }
  // Fallback to English if key not found in current lang
  if (v == null) {
    v = translations.en
    for (const p of parts) {
      if (v == null) break
      v = v[p]
    }
  }
  if (v == null) return key
  let str = String(v)
  if (vars) {
    Object.entries(vars).forEach(([k, val]) => {
      str = str.replace(new RegExp(`\\{\\{${k}\\}\\}`, 'g'), String(val))
    })
  }
  return str
}

// ── Standalone t() function ──────────────────────────────────────────────────
export function t(key, vars) {
  const lang = (typeof localStorage !== 'undefined' && localStorage.getItem('language')) || 'en'
  return translate(key, lang, vars)
}

// ── useLang hook ─────────────────────────────────────────────────────────────
export function useLang() {
  const [lang, setLangState] = useState(() => (typeof localStorage !== 'undefined' && localStorage.getItem('language')) || 'en')

  useEffect(() => {
    const handler = () => setLangState((typeof localStorage !== 'undefined' && localStorage.getItem('language')) || 'en')
    window.addEventListener('langchange', handler)
    return () => window.removeEventListener('langchange', handler)
  }, [])

  const setLang = (l) => {
    if (typeof localStorage !== 'undefined') localStorage.setItem('language', l)
    window.dispatchEvent(new Event('langchange'))
  }

  return {
    lang,
    setLang,
    t: (key, vars) => translate(key, lang, vars),
  }
}
